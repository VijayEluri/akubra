package org.fedoracommons.akubra.fs;

import java.net.URI;

import java.util.Map;

/**
 * Allocates unique filesystem paths.
 *
 * @author Chris Wilper
 */
public interface PathAllocator {
  /**
   * Allocates a new path.
   *
   * The path is guaranteed to be unique, and therefore must not be
   * solely based upon the blobId, if provided.
   *
   * @param blobId id of the blob to be stored, may be null.
   *        If provided, an implementation may or may not use the
   *        blobId for path allocation.
   * @param hints hints that implementations may use for allocation,
   *        may be null.
   * @return the path.
   */
  String allocate(URI blobId, Map<String, String> hints);

  /**
   * Gets the blobId from the given path, if possible.
   *
   * @param path the path (previously generated by this type of allocator)
   *        from which to extract the blobId.
   * @return the blobId if it can be determined from the path,
   *         null otherwise.
   */
  URI getBlobId(String path);
}
